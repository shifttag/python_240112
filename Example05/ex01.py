'''
관계 연산자
- 두 개의 항을 비교하여 그 결과를 논리(bool) 자료형으로
반환되는 연산
>, <, >=(> =), <=(< =), ==(= =), !=(! =)
'''
a = 100; b = 100
print(a > b)    # a는 b보다 크다 ? False
print(a < b)    # a는 b보다 작다 ? False
print(a >= b)   # a는 b보다 크거나 같다 ? True
print(a <= b)   # a는 b보다 작거나 같다 ? True
print(a == b)   # a와 b는 같다 ? True
print(a != b)   # a와 b는 같지 않다 ? False